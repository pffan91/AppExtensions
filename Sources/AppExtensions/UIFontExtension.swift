import UIKit

public extension UIFont {

    /// Manrope Regular
    static func manropeRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-Regular", size: size)!
    }

    /// Manrope Bold
    static func manropeBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-Bold", size: size)!
    }

    /// Manrope ExtraBold
    static func manropeExtraBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-ExtraBold", size: size)!
    }

    /// Manrope ExtraLight
    static func manropeExtraLight(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-ExtraLight", size: size)!
    }

    /// Manrope Light
    static func manropeLight(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-Light", size: size)!
    }

    /// Manrope Medium
    static func manropeMedium(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-Medium", size: size)!
    }

    /// Manrope SemiBold
    static func manropeSemiBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "Manrope-SemiBold", size: size)!
    }
}

